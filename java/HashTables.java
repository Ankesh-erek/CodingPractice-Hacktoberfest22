import java.util.Hashtable;

public class HashTables {
    public static void main(String[] args) {
        Hashtable<String , Integer> ht = new Hashtable<>();
        ht.put("Helllo"  , 1);
        ht.put("Sameekshya"  , 2);
        ht.put("Ashutosh"  , 3);
        ht.put("Ashu"  , 4);
        ht.put("ROni"  , 5);
        System.out.println(ht);
    }
}
